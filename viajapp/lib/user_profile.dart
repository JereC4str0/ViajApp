// lib/user_profile.dart
class UserProfile {
  final String name;
  final String email;
  final String phone;
  final String vehicleModel;
  final String vehiclePlate;
  final String vehicleType;

  UserProfile({
    required this.name,
    required this.email,
    required this.phone,
    required this.vehicleModel,
    required this.vehiclePlate,
    required this.vehicleType,
  });
}
